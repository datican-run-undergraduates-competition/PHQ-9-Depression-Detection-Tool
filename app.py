def get_severity_level(score: int) -> str:
    """Determine severity level based on PHQ-9 score"""
    if score <= 4:
        return 'minimal'
    elif score <= 9:
        return 'mild'
    elif score <= 14:
        return 'moderate'
    else:
        return 'severe'

